#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len_str;
	int		i;
	char	*str;

	i = 0;
	len_str = ft_strlen(s1);
	str = malloc(sizeof(char) * (len_str) + 1);
	if (!str)
		return (0);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
