int	print_map(char **map)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (map[j])
	{
		printf("%s\n", map[j]);
		while (map[j][i])
		{
			i++;
		}
		i = 0;
		j++;
	}
	printf("\n");
	return (1);
}